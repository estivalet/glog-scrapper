package glog.scrapper.msxgamesworld;

/**
 * http://msxgamesworld.com/gamecard.php?id=416
 * 
 * @author luisoft
 *
 */
public class MSXGamesWorldGameRelease {

	// Product code
	private String productCode;
	// Publisher
	private String publisher;
	// Year
	private String year;
	// Languages
	private String languages;
	// Region/Country
	private String region;
	// Format
	private String format;
	// Price
	private String price;
	// Dep. Legal
	private String depLegal;

	public String getProductCode() {
		return productCode;
	}

	public void setProductCode(String productCode) {
		this.productCode = productCode;
	}

	public String getPublisher() {
		return publisher;
	}

	public void setPublisher(String publisher) {
		this.publisher = publisher;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getLanguages() {
		return languages;
	}

	public void setLanguages(String languages) {
		this.languages = languages;
	}

	public String getRegion() {
		return region;
	}

	public void setRegion(String region) {
		this.region = region;
	}

	public String getFormat() {
		return format;
	}

	public void setFormat(String format) {
		this.format = format;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getDepLegal() {
		return depLegal;
	}

	public void setDepLegal(String depLegal) {
		this.depLegal = depLegal;
	}

}
